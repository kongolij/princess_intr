package com.bigcommerce.imports.catalog.product.dto;

public class OptionValue {

	public String id;
	public String option_name_en;
	public String option_value_en;
	public String option_name_fr_CA;
	public String option_value_fr_CA;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getOption_name_en() {
		return option_name_en;
	}

	public void setOption_name_en(String option_name_en) {
		this.option_name_en = option_name_en;
	}

	public String getOption_value_en() {
		return option_value_en;
	}

	public void setOption_value_en(String option_value_en) {
		this.option_value_en = option_value_en;
	}

	public String getOption_name_fr_CA() {
		return option_name_fr_CA;
	}

	public void setOption_name_fr_CA(String option_name_fr_CA) {
		this.option_name_fr_CA = option_name_fr_CA;
	}

	public String getOption_value_fr_CA() {
		return option_value_fr_CA;
	}

	public void setOption_value_fr_CA(String option_value_fr_CA) {
		this.option_value_fr_CA = option_value_fr_CA;
	}

}
