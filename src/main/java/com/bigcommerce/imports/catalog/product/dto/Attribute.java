package com.bigcommerce.imports.catalog.product.dto;

public class Attribute {

	public String id;
	public String en;
	public String fr_CA;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getEn() {
		return en;
	}

	public void setEn(String en) {
		this.en = en;
	}

	public String getFr_CA() {
		return fr_CA;
	}

	public void setFr_CA(String fr_CA) {
		this.fr_CA = fr_CA;
	}

}
